package com.active.lk.model;

public class BookingNumbers {
	
	private String today;
	
	private long bookedSlotsForToday;
	
	private long availableSlotsForToday;

	public String getToday() {
		return today;
	}

	public void setToday(String today) {
		this.today = today;
	}

	public long getBookedSlotsForToday() {
		return bookedSlotsForToday;
	}

	public void setBookedSlotsForToday(long bookedSlotsForToday) {
		this.bookedSlotsForToday = bookedSlotsForToday;
	}

	public long getAvailableSlotsForToday() {
		return availableSlotsForToday;
	}

	public void setAvailableSlotsForToday(long availableSlotsForToday) {
		this.availableSlotsForToday = availableSlotsForToday;
	}
	
	
	
	
	

}
